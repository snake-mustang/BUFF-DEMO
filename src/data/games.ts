// 获取所有游戏数据
export const allGames: Game[] = [...bestsellerGames, ...specialOfferGames, ...hotGames]

// 根据 URL 获取游戏详情
export const getGameByUrl = (url: string): Game | undefined => {
  return allGames.find(game => game.url === url)
}

// 根据 ID 获取游戏详情
export const getGameById = (id: string): Game | undefined => {
  return allGames.find(game => game.id === id)
}
